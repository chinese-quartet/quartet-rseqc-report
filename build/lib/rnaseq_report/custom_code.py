#!/usr/bin/env python
""" rnaseq-report plugin functions

We can add any custom Python functions here and call them
using the setuptools plugin hooks.
"""

from __future__ import print_function
from pkg_resources import get_distribution
import logging

from multiqc.utils import report, util_functions, config

# Initialise the main MultiQC logger
log = logging.getLogger('multiqc')

# Save this plugin's version number (defined in setup.py) to the MultiQC config
config.rnaseq_report_version = get_distribution("rnaseq_report").version


# Add default config options for the things that are used in MultiQC_NGI
def rnaseq_report_execution_start():
    """ Code to execute after the config files and
    command line flags have been parsedself.

    This setuptools hook is the earliest that will be able
    to use custom command line flags.
    """

    log.info("Running Example MultiQC Plugin v{}".format(config.rnaseq_report_version))

    # Add to the main MultiQC config object.
    # User config files have already been loaded at this point
    #   so we check whether the value is already set. This is to avoid
    #   clobbering values that have been customised by users.

    # Add to the search patterns used by gene_expression modules
    if 'rnaseq_deg_report/all_gene_boxplot' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/all_gene_boxplot': { 'fn_re': '^all_gene_exp.txt$' } } )

    if 'rnaseq_deg_report/all_gene_pca' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/all_gene_pca': { 'fn_re': '^all_gene_pca.txt$' } } )

    # Add to the search patterns used by modules
    if 'rnaseq_deg_report/deg_gene_pca' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_gene_pca': { 'fn_re': '^deg_pca.txt$' } } )

    if 'rnaseq_deg_report/deg_gene_volcano' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_gene_volcano': { 'fn_re': '^deg_fc.txt$' } } )

    if 'rnaseq_deg_report/deg_gene_heatmap' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_gene_heatmap': { 'fn_re': '^deg_exp.txt$' } } )

    # Add to the search patterns used by func_analysis modules
    if 'rnaseq_deg_report/deg_kegg' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_kegg': { 'fn_re': '^kegg.txt$' } } )

    if 'rnaseq_deg_report/deg_go_bp' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_go_bp': { 'fn_re': '^go_bp.txt$' } } )

    if 'rnaseq_deg_report/deg_go_cc' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_go_cc': { 'fn_re': '^go_cc.txt$' } } )

    if 'rnaseq_deg_report/deg_go_mf' not in config.sp:
        config.update_dict( config.sp, { 'rnaseq_deg_report/deg_go_mf': { 'fn_re': '^go_mf.txt$' } } )

    # # Some additional filename cleaning
    # config.fn_clean_exts.extend([
    #     '.my_tool_extension',
    #     '.removeMetoo'
    # ])

    # # Ignore some files generated by the custom pipeline
    # config.fn_ignore_paths.extend([
    #     '*/my_awesome_pipeline/fake_news/*',
    #     '*/my_awesome_pipeline/red_herrings/*',
    #     '*/my_awesome_pipeline/noisy_data/*',
    #     '*/my_awesome_pipeline/rubbish/*'
    # ])

    config.module_order = ['fastqc', 'fastq_screen', 'qualimap', 'rnaseq_expression', 'rnaseq_deg_report', 'rnaseq_func_analysis']

    config.log_filesize_limit = 2000000000